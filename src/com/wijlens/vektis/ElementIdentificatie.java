package com.wijlens.vektis;

/**
 * 
 * @author joris
 *
 */
public class ElementIdentificatie {

	private String id;

	public ElementIdentificatie(String id) {
		super();
		this.id = id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ElementIdentificatie other = (ElementIdentificatie) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "ElementIdentificatie [id=" + id + "]";
	}
	
}
